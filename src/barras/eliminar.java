    private void jbtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBorrarActionPerformed
        // TODO add your handling code her
        if(this.jcbIdMateria.getSelectedItem() == null){
            JOptionPane.showMessageDialog(this, "Nada que borrar","Borrar", JOptionPane.WARNING_MESSAGE);
            
            
            inicio p = new inicio();
            p.setLocationRelativeTo(this);
            p.setVisible(true);
            
            this.dispose();
            
        }
        else{
            if(BD.eliminarMateria((String)this.jcbIdMateria.getSelectedItem(), Sesion.usuarioActual,BD.obtenerHoraMateria(Sesion.usuarioActual, Sesion.materiaActual))){
                JOptionPane.showMessageDialog(this, "Se borr√≥ la materia !","Borrar",JOptionPane.INFORMATION_MESSAGE);
                
                this.dispose();
                
                inicio p = new inicio();
                p.setLocationRelativeTo(this);
                p.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(this, "No se pudo borrar la materia !","Borrar",JOptionPane.ERROR_MESSAGE);
                this.dispose();
                
                inicio p = new inicio();
                p.setLocationRelativeTo(this);
                p.setVisible(true);
            }
            
        }
      
    }//GEN-LAST:event_jbtnBorrarActionPerformed

